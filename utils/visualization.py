def color_name_to_rgb() -> Dict[str, Tuple[int, int, int]]:
    colors_rgb = []
    # Use matplotlib's color names
    for key, value in mcolors.cnames.items():
        # mcolors.cnames values are hex strings like #RRGGBB
        colors_rgb.append((key, struct.unpack('BBB', bytes.fromhex(value.replace('#', '')))))
    return dict(colors_rgb)


def draw_boxes_and_labels(
        boxes: np.ndarray,
        classes: np.ndarray,
        scores: Optional[np.ndarray],
        category_index: Dict[int, Dict[str, Any]],
        instance_masks: Optional[np.ndarray] = None,
        keypoints: Optional[np.ndarray] = None,
        max_boxes_to_draw: int = 20,
        min_score_thresh: float = 0.5,
        agnostic_mode: bool = False) -> Tuple[List[Dict[str, float]], List[List[str]], List[Tuple[int, int, int]]]:
    """Returns boxes coordinates, class names and colors
    """
    # Create a display string (and color) for every box location, group any boxes
    # that correspond to the same location.
    box_to_display_str_map = collections.defaultdict(list)
    box_to_color_map = collections.defaultdict(str)
    box_to_instance_masks_map = {}
    box_to_keypoints_map = collections.defaultdict(list)
    
    if not max_boxes_to_draw:
        max_boxes_to_draw = boxes.shape[0]
        
    for i in range(min(max_boxes_to_draw, boxes.shape[0])):
        if scores is None or scores[i] > min_score_thresh:
            box = tuple(boxes[i].tolist())
            if instance_masks is not None:
                box_to_instance_masks_map[box] = instance_masks[i]
            if keypoints is not None:
                box_to_keypoints_map[box].extend(keypoints[i])
            if scores is None:
                box_to_color_map[box] = 'black'
            else:
                if not agnostic_mode:
                    if classes[i] in category_index.keys():
                        class_name = category_index[classes[i]]['name']
                    else:
                        class_name = 'N/A'
                    display_str = '{}: {}%'.format(
                        class_name,
                        int(100 * scores[i]))
                else:
                    display_str = 'score: {}%'.format(int(100 * scores[i]))
                
                box_to_display_str_map[box].append(display_str)
                
                if agnostic_mode:
                    box_to_color_map[box] = 'DarkOrange'
                else:
                    box_to_color_map[box] = standard_colors()[
                        classes[i] % len(standard_colors())]

    # Store all the coordinates of the boxes, class names and colors
    color_rgb = color_name_to_rgb()
    rect_points = []
    class_names = []
    class_colors = []
    
    for box, color in six.iteritems(box_to_color_map):
        ymin, xmin, ymax, xmax = box
        rect_points.append(dict(ymin=ymin, xmin=xmin, ymax=ymax, xmax=xmax))
        class_names.append(box_to_display_str_map[box])
        
        # Safe access to color map
        color_lower = color.lower()
        if color_lower in color_rgb:
             class_colors.append(color_rgb[color_lower])
        else:
             # Fallback to black if color not found
             class_colors.append((0, 0, 0))
             
    return rect_points, class_names, class_colors
